package TreinametoKotlin

fun main(args: Array<String>) {

    // Somar        ( + )
    // Subtrair     ( - )
    // Multioplicar ( * )
    // DIvidir      ( / )
    // Modulo       ( % )

    println(5 + 5)
    println(10 - 5)
    println(10 / 2)
    println(20 % 2)

    var numero1 = 10
    var numero2 = 5
    var resultado = numero1 * numero2
    var resultado2 = numero1 / numero2

    println( resultado )
    println( resultado2 )

}